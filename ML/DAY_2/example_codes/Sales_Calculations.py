units_sold = 120 
price_per_unit = 250 
cost_per_unit = 180 
 
revenue = units_sold * price_per_unit 
total_cost = units_sold * cost_per_unit 
profit = revenue - total_cost 
 
print("Revenue:", revenue) 
print("Total cost:", total_cost) 
print("Profit:", profit) 


'''Profit: 8400 
Explanation: Revenue is units times selling price; cost is units times cost price; profit is the 
difference. This is the foundation of business analytics. '''