price = 2000 
discount_percent = 15 
discount_amount = price * discount_percent / 100 
final_price = price - discount_amount
print("Discount amount:", discount_amount) 
print("Final price:", final_price) 



'''Explanation: We compute 15% of 2000 (= 300), then subtract it from the original price. This kind of 
calculation appears constantly in e-commerce analytics. '''